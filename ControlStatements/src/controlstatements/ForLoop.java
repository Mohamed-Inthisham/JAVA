public class ForLoop {
    
    public static void main(String[]args){
        
        // This has 4 parts
        
        for( int i=0 ; i<5 ; i++){
            System.out.println("hellow");
        }
        // start - int i=0;
        // condition - i<5;
        // loop body - System.out.println("hellow");
        // increment/decrement - i++;
    }
    
}
