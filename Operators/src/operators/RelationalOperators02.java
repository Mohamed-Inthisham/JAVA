public class RelationalOperators02 {
   
    public static void main(String[]args){
        // Greater than >
        System.out.println(50 > 10); // true
        System.out.println(50 > 60);
        
        // Greter than or equal >= 
        System.out.println(50 >= 10); //true
        System.out.println(50 >= 50); //true
        System.out.println(50 >= 65);
        
        // Less than <
        System.out.println(20 < 60); // true
        System.out.println(20 < 18);
        
        // Less than or equal <=
        System.out.println(20 <= 40); //true
        System.out.println(20 <= 20); // true
        System.out.println(20 <= 16);
        
        // equals ==
        System.out.println(20 == 20); // true
        System.out.println(20 == 48);
        
        
        // not equals !=
        System.out.println(50 != 75); // true
        System.out.println(50 != 50);
    }
    
}
